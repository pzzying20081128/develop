package cn.zy.apps.tools.units.moneys ;

public class BuildMoneyException extends RuntimeException {

    private static final long serialVersionUID = -1908173333521140905L ;

    public BuildMoneyException() {
       
    }

    public BuildMoneyException(String message) {
        super(message) ;
       
    }

    public BuildMoneyException(Throwable cause) {
        super(cause) ;
       
    }

    public BuildMoneyException(String message, Throwable cause) {
        super(message, cause) ;
       
    }

    public BuildMoneyException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace) ;
       
    }

}
